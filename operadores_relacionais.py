a = 6
b = 9

# Igualdade (==)
resultado = a == b
print("###########################")
print("")
print(f"{a} e {b}")
print("")

print(f"Igualdade: {resultado}")

# Diferença (!=)
resultado = a != b
print(f"Diferença: {resultado}")

# Maior que (>)
resultado = a > b
print(f"Maior que: {resultado}")

# Menor que (<)
resultado = a < b
print(f"Menor que: {resultado}")

# Maior ou igual (>=)
resultado = a >= b
print(f"Menor ou igual: {resultado}")

# Menor ou igual (<=)
resultado = a <= b
print(f"Menor ou igual: {resultado}")
print("")
print("###########################")